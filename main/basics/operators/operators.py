# Exponential
n = 6**2 + 6
print(n)  
    # 42

# Bitwise XOR
n = 6^2
print(n) 
    # 4

# Division
n = 105 / 60
print(n) 
    # 1.74

# Floor Division
n = 105 // 60
print(n) 
    # 1

# Remainder example - with floor division
minutes = 105
hours = minutes // 60
remainder = minutes - hours*60
print(remainder) 
    #45

# Remainder example - with modulo
minutes = 105
remainder = 105 % 60
print(remainder)
    # 45